       private String[] llenarUsuario(String escuela)
    {
        String[] carrera = new String[1];
        
        if(escuela.equalsIgnoreCase("Selecciona"))
        {
            carrera[0] = "";
            
        }
        
        if(escuela.equalsIgnoreCase("Administrador"))
        {
            carrera[0] = "1";
            
        }
        
        if(escuela.equalsIgnoreCase("Usuario"))
        {
            carrera[0] = "2";
            
        }
        
      
        
        return carrera;
    }
    
